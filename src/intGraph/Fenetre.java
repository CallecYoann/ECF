    private void jButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonActionPerformed
        String nomVoilier = txtFieldNomVoilier.getText();
        String strNumVoilier = txtFieldNumVoilier.getText();

        Object objprop = proprietaireComboBox.getSelectedItem();
        Object objclass = classeComboBox.getSelectedItem();

        Proprietaire proprietaire = (Proprietaire) objprop;
        Classe classe = (Classe) objclass;
        if (nomVoilier.isEmpty() || strNumVoilier.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Tout les champs doivent etre remplis");
        } else {

            try {
                int NumVoilier = Integer.parseInt(strNumVoilier);
                Voilier voil = new Voilier(nomVoilier, NumVoilier, proprietaire, classe);
                VoilierDAO.saveVoilier(voil);
                vtm.addVoilier(voil);
                vtm.fireTableDataChanged();
                txtFieldNomVoilier.setText("");
                txtFieldNumVoilier.setText("");

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "dsgfsdfdsf");
                e.printStackTrace();
            }
        }

    }//GEN-LAST:event_jButtonActionPerformed

