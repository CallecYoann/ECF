    private void addProprietaireButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addProprietaireButtonActionPerformed
        String nomProprietaire = txtFieldNomProprietaire.getText();
        String prenomProprietaire = txtFieldPrenomProprietaire.getText();
        String strAgeProprietaire = txtFieldAgeProprietaire.getText();
        String strNumLicenceProprietaire = txtFieldLicenceProprietaire.getText();
        String nomClub = txtFieldClubProprietaire.getText();

        if (nomProprietaire.isEmpty() || prenomProprietaire.isEmpty() || strAgeProprietaire.isEmpty() || strNumLicenceProprietaire.isEmpty() || nomClub.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Tout les champs doivent etre remplis");
        } else {

            try {
                int AgeProprietaire = Integer.parseInt(strAgeProprietaire);
                int NumLicenceProprietaire = Integer.parseInt(strNumLicenceProprietaire);
                Proprietaire prop = new Proprietaire(nomProprietaire, prenomProprietaire, AgeProprietaire, NumLicenceProprietaire, nomClub);
                ProprietaireDAO.saveProprietaire(prop);
                txtFieldNomProprietaire.setText("");
                txtFieldPrenomProprietaire.setText("");
                txtFieldAgeProprietaire.setText("");
                txtFieldLicenceProprietaire.setText("");
                txtFieldClubProprietaire.setText("");

            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_addProprietaireButtonActionPerformed

